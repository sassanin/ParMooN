void NF_D_T_P3_3D_EvalAll(TCollection *Coll, TBaseCell *Cell, double *PointValues, double *Functionals)
{
  int i;
  double s;

  s = 0;
  //constant
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[0] = s;

  s = 0;
  //x
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[1] = s;

  s = 0;
  //y
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[2] = s;

  s = 0;
  //z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[3] = s;

  s = 0;
  //x*x
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[4] = s;

  s = 0;
  //x*y
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[5] = s;

  s = 0;
  //x*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[6] = s;

  s = 0;
  //yy
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[7] = s;

  s = 0;
  //y*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[8] = s;

  s = 0;
  //zz
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[9] = s;

  s = 0;
  //x*x*x
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[10] = s;

  s = 0;
  //x*x*y
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[11] = s;

  s = 0;
  //x*x*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[12] = s;

  s = 0;
  //x*y*y
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[13] = s;

  s = 0;
  //x*y*x
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[14] = s;

  s = 0;
  //x*z*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Xi[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[15] = s;

  s = 0;
  //y*y*y
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[16] = s;

  s = 0;
  //y*y*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[17] = s;

  s = 0;
  //y*z*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Eta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[18] = s;

  s = 0;
  //z*z*z
  for(i=0;i<43;i++)
    s += PointValues[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Zeta[i] * NF_D_T_P3_3D_Weights[i];
  Functionals[19] = s;
}

